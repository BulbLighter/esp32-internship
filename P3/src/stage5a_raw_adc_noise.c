// PROJECT 3 — Stage 5A: ADC Noise Measurement (WITH capacitor filter)
// Print 100 rapid readings to see jitter

const int LDR_PIN = 35;

void setup() {
  Serial.begin(115200);
  delay(1000);
  analogSetAttenuation(ADC_11db);
  
  Serial.println("=== Stage 5A: ADC Noise (WITH 100nF capacitor) ===");
  Serial.println("Hold LDR in constant light, taking 100 rapid readings...");
  Serial.println();
}

void loop() {
  // Read ADC 100 times rapidly (no delay between reads)
  for (int i = 0; i < 100; i++) {
    int raw = analogRead(LDR_PIN);
    Serial.print("Read ");
    Serial.print(i + 1);
    Serial.print(": ");
    Serial.println(raw);
  }
  
  Serial.println();
  Serial.println("=== 100 reads complete. Pausing 5 seconds... ===");
  Serial.println();
  delay(5000);
}


