// PROJECT 3 — Stage 5B: Software Averaging (WITH capacitor + averaging)

const int LDR_PIN = 35;
const int NUM_SAMPLES = 16;

void setup() {
  Serial.begin(115200);
  delay(1000);
  analogSetAttenuation(ADC_11db);
  
  Serial.println("=== Stage 5B: Filtered Reading (Capacitor + 16-sample average) ===");
  Serial.println();
}

void loop() {
  long sum = 0;
  
  for (int i = 0; i < NUM_SAMPLES; i++) {
    sum += analogRead(LDR_PIN);
    delayMicroseconds(100);  // small gap between samples
  }
  
  int averaged = sum / NUM_SAMPLES;
  
  Serial.print("Averaged ADC: ");
  Serial.println(averaged);
  
  delay(200);
}


